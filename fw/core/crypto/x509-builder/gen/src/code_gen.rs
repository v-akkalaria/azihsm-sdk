//! Rust source code emitter for TBS templates and offset constants.
//!
//! Produces a Rust source file containing:
//! - A copyright header and auto-generation notice.
//! - A `pub const TBS_TEMPLATE: [u8; N]` array with the sanitized TBS bytes.
//! - `pub const <FIELD>_OFFSET: usize` and `pub const <FIELD>_LEN: usize`
//!   pairs for each variable field.

use crate::tbs::FieldOffset;

/// Emit a Rust source module for a TBS template and its field offset constants.
///
/// # Arguments
/// * `module_doc` — One-line module doc comment (written as `//! <text>`).
/// * `tbs` — Sanitized TBS byte array (placeholder bytes at variable positions).
/// * `fields` — Variable field descriptors (name, offset, length).
///
/// # Returns
/// A `String` containing the complete Rust source file.
pub fn emit_template_module(module_doc: &str, tbs: &[u8], fields: &[FieldOffset]) -> String {
    let mut out = String::new();

    out.push_str("// Copyright (c) Microsoft Corporation.\n");
    out.push_str("// Licensed under the MIT License.\n");
    out.push('\n');
    out.push_str("// AUTO-GENERATED by x509-gen. Do not edit manually.\n");
    out.push('\n');
    out.push_str(&format!("//! {module_doc}\n"));
    out.push('\n');

    // TBS template array
    out.push_str(
        "/// DER-encoded TBS template with placeholder bytes (0x5F) for variable fields.\n",
    );
    out.push_str(&format!(
        "pub const TBS_TEMPLATE: [u8; {}] = [\n",
        tbs.len()
    ));
    for chunk in tbs.chunks(16) {
        out.push_str("    ");
        for (i, byte) in chunk.iter().enumerate() {
            if i > 0 {
                out.push_str(", ");
            }
            out.push_str(&format!("0x{byte:02X}"));
        }
        out.push_str(",\n");
    }
    out.push_str("];\n\n");

    // Offset and length constants for each field
    for (i, field) in fields.iter().enumerate() {
        out.push_str(&format!(
            "/// Offset of the {} field in TBS_TEMPLATE.\n",
            field.name
        ));
        out.push_str(&format!(
            "pub const {}_OFFSET: usize = {};\n",
            field.name, field.offset
        ));
        out.push_str(&format!(
            "/// Length of the {} field in bytes.\n",
            field.name
        ));
        out.push_str(&format!(
            "pub const {}_LEN: usize = {};\n",
            field.name, field.len
        ));
        if i + 1 < fields.len() {
            out.push('\n');
        }
    }

    out
}

#[cfg(test)]
#[allow(clippy::unwrap_used)]
mod tests {
    use super::*;

    #[test]
    fn test_emit_template_module() {
        let tbs = vec![0x30, 0x03, 0x5F, 0x5F, 0x5F];
        let fields = vec![FieldOffset {
            name: "PUBLIC_KEY",
            offset: 2,
            len: 3,
        }];
        let output = emit_template_module("Test template", &tbs, &fields);
        assert!(output.contains("TBS_TEMPLATE"));
        assert!(output.contains("PUBLIC_KEY_OFFSET: usize = 2"));
        assert!(output.contains("PUBLIC_KEY_LEN: usize = 3"));
        assert!(output.contains("AUTO-GENERATED"));
    }
}
